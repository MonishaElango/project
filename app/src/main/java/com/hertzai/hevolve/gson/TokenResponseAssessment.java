package com.hertzai.hevolve.gson;

public class TokenResponseAssessment {




    private String access_token_assess;
    private String refresh_token_assess;
    private String token_name_assess;
    private String token_type_assess;
    private int expires_in_assess;

    public String getRefresh_token_assess() {
        return refresh_token_assess;
    }

    public void setRefresh_token_assess(String refresh_token_assess) {
        this.refresh_token_assess = refresh_token_assess;
    }

    public String getToken_name_assess() {
        return token_name_assess;
    }

    public void setToken_name_assess(String token_name_assess) {
        this.token_name_assess = token_name_assess;
    }

    public String getToken_type_assess() {
        return token_type_assess;
    }

    public void setToken_type_assess(String token_type_assess) {
        this.token_type_assess = token_type_assess;
    }

    public int getExpires_in_assess() {
        return expires_in_assess;
    }

    public void setExpires_in_assess(int expires_in_assess) {
        this.expires_in_assess = expires_in_assess;
    }

    public String getAccess_token_assess() {
        return access_token_assess;
    }

    public void setAccess_token_assess(String access_token_assess) {
        this.access_token_assess = access_token_assess;
    }
}
